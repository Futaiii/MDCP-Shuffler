func (db *DB) createTable() error {
	// 移除 image_path, 将 level 改为 REAL (for floats)
	query := `
    CREATE TABLE IF NOT EXISTS songs (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        title TEXT NOT NULL,
        artist TEXT NOT NULL,
        level REAL,
        color TEXT,
        is_favorite BOOLEAN DEFAULT 0
    )`
	_, err := db.conn.Exec(query)
	return err
}

func (db *DB) GetAllSongs() ([]models.Song, error) {
	// 查询中移除 image_path
	rows, err := db.conn.Query(`
        SELECT id, title, artist, level, color, is_favorite 
        FROM songs ORDER BY title ASC
    `)
	if err != nil {
		return nil, err
	}
	defer rows.Close()

	var songs []models.Song
	for rows.Next() {
		var s models.Song
		// Scan 中移除 ImagePath
		err := rows.Scan(&s.ID, &s.Title, &s.Artist,
			&s.Level, &s.Color, &s.IsFavorite)
		if err != nil {
			return nil, err
		}
		songs = append(songs, s)
	}
	return songs, nil
}

func (db *DB) AddSong(song models.Song) error {
	// INSERT 语句中移除 image_path
	_, err := db.conn.Exec(
		"INSERT INTO songs (title, artist, level, color, is_favorite) VALUES (?, ?, ?, ?, ?)",
		song.Title, song.Artist, song.Level, song.Color, song.IsFavorite,
	)
	return err
}

func (db *DB) UpdateSong(song models.Song) error {
	// UPDATE 语句中移除 image_path
	_, err := db.conn.Exec(
		"UPDATE songs SET title=?, artist=?, level=?, color=?, is_favorite=? WHERE id=?",
		song.Title, song.Artist, song.Level, song.Color, song.IsFavorite, song.ID,
	)
	return err
}

func (db *DB) DeleteSong(id int) error {
	// 记录即将删除的歌曲 id
	log.Printf("Attempting to delete song with id=%d", id)

	// 执行删除语句
	res, err := db.conn.Exec("DELETE FROM songs WHERE id=?", id)
	if err != nil {
		// 删除失败，记录错误并返回
		log.Printf("Error deleting song id=%d: %v", id, err)
		return err
	}

	// 可选：检查实际受影响的行数，确保真的删除了记录
	if rows, _ := res.RowsAffected(); rows == 0 {
		log.Printf("No song found with id=%d", id)
	} else {
		log.Printf("Successfully deleted song with id=%d (rows affected: %d)", id, rows)
	}
	return nil
}

func (db *DB) ToggleFavorite(id int) error {
	_, err := db.conn.Exec("UPDATE songs SET is_favorite = NOT is_favorite WHERE id=?", id)
	return err
}

func (db *DB) Close() error {
	return db.conn.Close()
}

func (db *DB) GetSongByID(id int) (models.Song, error) {
	// 查询和 Scan 中移除 image_path
	row := db.conn.QueryRow(`
        SELECT id, title, artist, COALESCE(level, 0), color, is_favorite 
        FROM songs WHERE id = ?
    `, id)

	var s models.Song
	err := row.Scan(&s.ID, &s.Title, &s.Artist,
		&s.Level, &s.Color, &s.IsFavorite)
	return s, err
}
