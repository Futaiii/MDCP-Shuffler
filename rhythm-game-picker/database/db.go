type DB struct {
	conn *sql.DB
}

func InitializeDatabase(ctx context.Context) (*sql.DB, error) {
	appDataDir, err := os.UserHomeDir()
	if err != nil {
		return nil, err
	}
	dbDir := filepath.Join(appDataDir, ".shuffle")
	if err := os.MkdirAll(dbDir, 0755); err != nil {
		return nil, err
	}
	dbPath := filepath.Join(dbDir, "shuffle.db")

	db, err := sql.Open("sqlite3", dbPath)
	if err != nil {
		return nil, err
	}
	return db, nil
}

func NewDB(ctx context.Context) (*DB, error) {
	conn, err := InitializeDatabase(ctx)
	if err != nil {
		return nil, err
	}

	db := &DB{conn: conn}
	if err := db.createTable(); err != nil {
		return nil, err
	}

	// 新增：调用数据填充函数
	if err := db.seedInitialData(); err != nil {
		return nil, err
	}

	return db, nil
}

